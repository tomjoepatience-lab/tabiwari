// 品名（＋店名）から1明細のジャンルを推定する
export function classifyItem(itemName: string, storeName?: string | null): Genre {
  const name = (itemName || '').trim();
  const store = (storeName || '').trim();
  // 外食店での注文は品名（うどん・刺身…）に関わらず外食（店の文脈が最優先）
  if (store && STORE_EATOUT.test(store)) return '外食';
  for (const [genre, rx] of ITEM_RULES) {
    if (rx.test(name)) return genre;
  }
  if (store) {
    for (const [genre, rx] of STORE_RULES) {
      if (rx.test(store)) return genre;
    }
  }
  return 'その他';
}

export function isGenre(v: unknown): v is Genre {
  return typeof v === 'string' && (GENRES as readonly string[]).includes(v);
}
